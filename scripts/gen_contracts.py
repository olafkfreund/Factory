#!/usr/bin/env python3
"""Generate the factory-contracts single source of truth (Factory#160, epic #154).

The completion-event / token-usage contract and the lifecycle status taxonomy are
currently hand-rolled per service and disagree at runtime (CFactory, AIFactory,
PFactory and TFactory each re-derive ``CompletionEvent`` / ``Usage``; the status
classifier was reimplemented five divergent times in CFactory alone). This script
generates ONE pinned, committed ``factory-contracts`` artifact from the hub's
canonical inputs so consumers can pin it instead of re-deriving:

    apis/completion-events.asyncapi.yaml  -> RFC-0001 CompletionEvent + Usage
    apis/status-taxonomy.json             -> lifecycle token sets + classifiers

Outputs (committed so consumers pin a known version):
    shared/factory-contracts/python/factory_contracts/__init__.py   (pydantic v2)
    shared/factory-contracts/ts/factory-contracts.ts                (TS types)

Generation is intentionally dependency-free: ``datamodel-code-generator`` /
``json-schema-to-typescript`` are NOT vendored into the hub devShell, and the two
contracts are small and stable, so a tiny self-contained emitter is preferable to
a heavy toolchain dependency. If those tools are later adopted fleet-wide this
module is the single place to swap the emitter; the generated public API is the
contract, not the generator.

The Python model is hand-shaped (not a blind schema dump) so it carries the
RFC-0001 envelope, the v1.1 optional ``usage`` block, the usage rollup helpers
(``add_usage`` / ``rollup_usage`` per the issue), and the status classifiers
(``is_terminal`` / ``is_failed`` / ...) mirroring CFactory's canonical
``status_taxonomy.py`` token sets. The token sets themselves are read from
``status-taxonomy.json`` at generation time, so the JSON stays the source of truth.

Usage:
    python scripts/gen_contracts.py            # regenerate in place
    python scripts/gen_contracts.py --check    # fail if the committed output is stale
"""

from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path

_REPO_ROOT = Path(__file__).resolve().parents[1]
_TAXONOMY_JSON = _REPO_ROOT / "apis" / "status-taxonomy.json"
_OUT_PY = (
    _REPO_ROOT / "shared" / "factory-contracts" / "python" / "factory_contracts" / "__init__.py"
)
_OUT_TS = _REPO_ROOT / "shared" / "factory-contracts" / "ts" / "factory-contracts.ts"

# Multi-line so the emitted docstring/comment line never exceeds the 100-col bar.
_GENERATED_BANNER = (
    "GENERATED by scripts/gen_contracts.py from\n"
    "apis/status-taxonomy.json + apis/completion-events.asyncapi.yaml.\n"
    "DO NOT EDIT BY HAND - rerun the generator (Factory#160)."
)
# TS comment banner: each line prefixed with '// ' (the docstring form is bare).
_GENERATED_BANNER_TS = _GENERATED_BANNER.replace("\n", "\n// ")

# The canonical state keys whose token sets drive both emitted artifacts. 'stuck'
# is intentionally a sibling state (anomaly heuristic), not part of terminal.
_STATE_ORDER = ("failed", "done", "review", "queued", "running", "stuck")


def _emit(message: str) -> None:
    # Generator CLI: its stdout report IS its purpose, so the no-print rule is
    # intentionally suppressed at the single output sink.
    print(message)  # noqa: T201


def load_taxonomy() -> dict[str, list[str]]:
    """Read the canonical token sets from ``status-taxonomy.json``.

    Returns a state -> sorted-token-list mapping for the states the generator
    emits, preserving the JSON as the single source of truth.
    """
    data = json.loads(_TAXONOMY_JSON.read_text(encoding="utf-8"))
    states = data["states"]
    return {state: sorted(states[state]["tokens"]) for state in _STATE_ORDER}


def _py_frozenset_literal(tokens: list[str]) -> str:
    """Render a token list as a stable, line-wrapped ``frozenset`` literal."""
    inner = ",\n".join(f'        "{tok}"' for tok in tokens)
    return "frozenset(\n    {\n" + inner + ",\n    }\n)"


def render_python(taxonomy: dict[str, list[str]]) -> str:
    """Render the pydantic v2 module + usage helpers + status classifiers."""
    token_blocks = "\n".join(
        f"_{state.upper()}_TOKENS: frozenset[str] = {_py_frozenset_literal(tokens)}"
        for state, tokens in taxonomy.items()
    )
    return _PY_TEMPLATE.format(banner=_GENERATED_BANNER, token_blocks=token_blocks)


def render_typescript(taxonomy: dict[str, list[str]]) -> str:
    """Render the TS types + token sets + classifier functions."""
    token_blocks = "\n".join(
        "export const {name}_TOKENS: ReadonlySet<string> = new Set([\n{items}\n]);".format(
            name=state.upper(),
            items="\n".join(f'  "{tok}",' for tok in tokens),
        )
        for state, tokens in taxonomy.items()
    )
    return _TS_TEMPLATE.format(banner=_GENERATED_BANNER_TS, token_blocks=token_blocks)


def _write_if_changed(path: Path, content: str, *, check: bool) -> bool:
    """Write *content* to *path* (mkdir -p). In check mode, never write.

    Returns True when the on-disk content already matches (or was written);
    False when it is stale (check mode only).
    """
    current = path.read_text(encoding="utf-8") if path.exists() else None
    if current == content:
        return True
    if check:
        return False
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content, encoding="utf-8")
    return True


def generate(*, check: bool) -> int:
    """Generate (or verify) both artifacts. Returns a process exit code."""
    taxonomy = load_taxonomy()
    targets = ((_OUT_PY, render_python(taxonomy)), (_OUT_TS, render_typescript(taxonomy)))
    stale: list[Path] = []
    for path, content in targets:
        ok = _write_if_changed(path, content, check=check)
        if not ok:
            stale.append(path)
        elif not check:
            _emit(f"wrote {path.relative_to(_REPO_ROOT)}")
    if check and stale:
        for path in stale:
            _emit(f"STALE: {path.relative_to(_REPO_ROOT)} (run scripts/gen_contracts.py)")
        return 1
    if check:
        _emit("factory-contracts generated output is up to date")
    return 0


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--check",
        action="store_true",
        help="Fail (exit 1) if the committed output is stale; do not write.",
    )
    args = parser.parse_args(argv)
    return generate(check=args.check)


